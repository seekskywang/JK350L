#ifndef __BEEP_H
#define	__BEEP_H


#include "stm32f7xx.h"


/* 定义蜂鸣器连接的GPIO端口, 用户只需要修改下面的代码即可改变控制的蜂鸣器引脚 */
#define BEEP_GPIO_PORT    	    GPIOC			              /* GPIO端口 */
#define BEEP_GPIO_CLK_ENABLE()  __HAL_RCC_GPIOC_CLK_ENABLE()  /* GPIO端口时钟 */
#define BEEP_GPIO_PIN		  	GPIO_PIN_1			          /* 连接到蜂鸣器的GPIO */

/* 高电平时，蜂鸣器响 */
#define ON  1
#define OFF 0

/* 带参宏，可以像内联函数一样使用 */
#define BEEP(a)	HAL_GPIO_WritePin(BEEP_GPIO_PORT,BEEP_GPIO_PIN,a)
					
/* 直接操作寄存器的方法控制IO */
#define	digitalHi(p,i)			{p->BSRR = i;}			        //设置为高电平		
#define digitalLo(p,i)			{p->BSRR = (uint32_t)i << 16;;}  //输出低电平
#define digitalToggle(p,i)	    {p->ODR ^=i;}			//输出反转状态


/* 定义控制IO的宏 */
#define BEEP_TOGGLE		digitalToggle(BEEP_GPIO_PORT,BEEP_GPIO_PIN)
#define BEEP_ON	        digitalHi(BEEP_GPIO_PORT,BEEP_GPIO_PIN)
#define BEEP_OFF		digitalLo(BEEP_GPIO_PORT,BEEP_GPIO_PIN)

void BEEP_GPIO_Config(void);
void Beep_task(void *p_arg);
extern OS_SEM  Beep_SEM;        //定义一个信号量，用于任务同步

//任务控制块
extern OS_TCB BeepTaskTCB;
//任务堆栈
extern CPU_STK Beep_TASK_STK[Beep_STK_SIZE];					
#endif /* __BEEP_H */
